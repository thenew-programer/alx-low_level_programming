#include "3-calc.h"


/**
* op_add - add two parameters
* @a: 1st parameter
* @b: 2nd parameter
* Return: Result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtract two parameters
* @a: 1st parameter
* @b: 2nd parameter
* Return: Result
*/

int op_sub(int a, int b)
{
	return (a - b);
}


/**
* op_mul - multiply two parameters
* @a: 1st parameter
* @b: 2nd parameter
* Return: Result
*/

int op_mul(int a, int b)
{
	return (a * b);
}



/**
* op_div - divide two parameters
* @a: 1st parameter
* @b: 2nd parameter
* Return: Result
*/

int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - modulo two parameters
* @a: 1st parameter
* @b: 2nd parameter
* Return: Result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
